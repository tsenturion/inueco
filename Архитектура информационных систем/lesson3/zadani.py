# Программа делает небольшое число операций с циклами

# Создание списка чисел от 1 до 10
numbers = list(range(1, 11))

# Объявление пустого списка для хранения результатов
squares = []

# Цикл для вычисления квадрата каждого числа
for num in numbers:
    square = num ** 2
    squares.append(square)

# Вывод исходных чисел и их квадратов
print("Числа и их квадраты:")

for i in range(len(numbers)):
    print(f"{numbers[i]}^2 = {squares[i]}")

# Еще один цикл для демонстрации
print("\nКвадраты чисел ускоренно:")

for num in range(11, 21):
    print(f"{num} в квадрате равно {num ** 2}")

# Итоговая информация
print("\nРабота программы завершена.")